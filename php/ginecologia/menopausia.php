  <h3 class="verde">Menopausia</h3>
  <p>
    <ul>
      <li>Es una fase de la vida.</li>
      <li>No es una enfermedad.</li>
      <li>Acudir a consulta al comenzar los ciclos irregulares, cuando se tengan sofocaciones o haya pérdidas o hemorragias menstruales.</li>
      <li>Durante esta fase aumenta el riesgo de enfermedades crónicas.</li>
      <li>Cada mujer necesita una atención diferente y un tratamiento personalizado.</li>
    </ul>
