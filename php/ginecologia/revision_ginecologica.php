  <h3 class="verde">Revisión Ginecológica</h3>
  <p>
    <ul>
      <li>Tiene como fin la prevención y detección precoz de alteraciónes del aparato sexual femenino en relación a las enfermedades de transmisión sexual, el cáncer ginecológico y/o el cáncer de mama.</li>
      <li>Una vez al año, todas las mujeres deben visitar a su ginecologo a fin de realizar una revisón preventiva y/o un diagnóstico precoz de las posibles patologías del aparato reproductor femenino.</li>
      <li>Esta visita irá acompañada de las necesarias pruebas complementarias a la exploración física.</li>
      <li>Pruebas complementarias:</li>
      <ul>
        <li>Citología Exo-Endocenical.</li>
        <li>Ecografía vaginal/abdominal.</li>
        <li>Análisis Clínicos.</li>
        <li>Colposcopia. Determinación VPH.</li>
        <li>Hiseroscopia diagnóstica.</li>
      </ul>
    </ul>
  </p>
