  <h3 class="verde">Vacuna del Virus del Papiloma HUmano (VPH)</h3>
  <p>
    <ul>
      <li>Protege de la infección futura , la cual pueda conducir al cáncer.</li>
      <li>Se recomienda en adolescentes (ambos sexos) entre los 9 y 1 años y mujeres de 16 a 26 años preferiblemente antes de comenzar a tener relaciones sexuales.</li>
      <li>La vacunación se realiza en tres sesiones por via intramuscular.</li>
      <li>Hay que continuar realizando controles ginecológicos y citológicos.</li>
      <li><i>Recuerde</i> el VPH es un virus que se transmite por via sexual</li>
      <ul>
        <li>Es facilmente transmisible.</li>
        <li>La infección por VPH es asintomática hasta los 6 o 24 meses.</li>
        <li>No existe tratamiento para la infección por VPH.</li>
        <li>Altera la citología.</li>
        <li>La mayor parte de los cánceres de cuello de útero son debidos a la infección del VPH.</li>
        <li>La vacunación previene muchos de estos cánceres.</li>
