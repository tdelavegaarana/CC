  <h3 class = "verde">Dolor Crónico, Fibromialgia, Estrés, etc.</h3>
  <ul>
    <li>GRUPOS DE MANEJO DEL DOLOR</li>
    <p>Entre el 10 y el 23% de los españoles sufren DOLOR CRÓNICO. El impacto de dolor crónico en la vida de las personas que lo padecen y de sus familias es altísimo. En el dolor influyen aspectos biológicos, psicológicos y sociales, y su tratamiento requiere de un abordaje interdisciplinar, esto es, médico o biológico, y psicosocial. Los tratamientos cognitivo-conductuales han mostrado eficacia en el abordaje del dolor crónico. Existen paquetes de tratamiento en formato grupal además del tratamiento individual para ayudar a las personas con esta problemática a sobrellevar mejor el dolor.<br>El <strong><u>TRATAMIENTO GRUPAL</u></strong> consiste en sesiones semanales o quincenales de 1 hora y media aproximada de duración,<br>Las sesiones están compuestas de los siguientes entrenamientos:<br></p>
    <ol>
      <li>Influencia de los factores psicológicos sobre el dolor: el por qué del abordaje psicológico en un problema desencadenado por factores orgánicos.</li>
      <li>Entrenamiento en respiración y relajación. Introducción al mindfulness.</li>
      <li>Manejo de la atención, su papel en la percepción del dolor. Técnicas de distracción e imaginación.</li>
      <li>Reestructuración cognitiva. Pensamientos automáticos y distorsiones cognitivas y estrategias para su modificación.</li>
      <li>Resolución de problemas.</li>
      <li>Manejo de emociones y asertividad.</li>
      <li>Valores vitales y establecimiento de objetivos.</li>
      <li>Organización del tiempo y actividades de refuerzo.</li>
      <li>Ejercicio físico, higiene postural y del sueño y prevención de recaidas.</li>
    </ol>
  </ul>
